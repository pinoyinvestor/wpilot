// ── Detect language from WordPress locale ──────────────────────
if ( ! function_exists( 'wpilot_get_lang' ) ) {
function wpilot_get_lang() {
    static $lang = null;
    if ( $lang !== null ) return $lang;
    $locale = get_locale(); // e.g. sv_SE, en_US, de_DE, fr_FR, es_ES, nb_NO, da_DK, fi, nl_NL, pl_PL, pt_BR
    $code   = strtolower( substr( $locale, 0, 2 ) );
    $supported = ['sv','en','de','fr','es','nb','da','fi','nl','pl','pt','it','ro','hu','cs','sk','hr','bg','ru','uk','tr','ja','ko','zh','ar'];
    $lang = in_array( $code, $supported, true ) ? $code : 'en';
    return $lang;
}
} // end wpilot_get_lang

// ── Translation function ───────────────────────────────────────
if ( ! function_exists( 'wpilot_t' ) ) {
function wpilot_t( $key ) {
    static $strings = null;
    if ( $strings === null ) $strings = wpilot_strings();
    $lang = wpilot_get_lang();
    if ( isset( $strings[ $key ][ $lang ] ) ) return $strings[ $key ][ $lang ];
    if ( isset( $strings[ $key ]['en'] )    ) return $strings[ $key ]['en'];
    return $key;
}
} // end wpilot_t

// ── Echo shortcut ──────────────────────────────────────────────
if ( ! function_exists( 'wpilot_te' ) ) {
function wpilot_te( $key ) {
    echo esc_html( wpilot_t( $key ) );
}
} // end wpilot_te

